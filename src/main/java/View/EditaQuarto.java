    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Quarto editquarto = new Quarto();

        editquarto.setId(this.id);
        editquarto.setNumero(Integer.parseInt(jTextField1.getText()));
        editquarto.setDisponibilidade(Boolean.getBoolean(jTextField3.getText()));
        editquarto.setPreco(Double.parseDouble(jTextField4.getText()));
        editquarto.setTipo(jTextField2.getText());
        
        repository.openConnection();
        repository.update(editquarto);
        repository.closeConnection();
        
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

