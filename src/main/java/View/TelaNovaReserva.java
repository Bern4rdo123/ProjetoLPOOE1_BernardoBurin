    JPAPersistence repository = new JPAPersistence();
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        repository.openConnection();

        // Buscar cliente e quarto no banco
        Cliente clientetoInsert = repository.find(Cliente.class,Integer.parseInt(jTextField3.getText())); // Altere para obter o ID do cliente.
        Quarto quartotoInsert = repository.find(Quarto.class, Integer.parseInt(jTextField4.getText()));   // Altere para obter o ID do quarto.

        // Validar se cliente e quarto foram encontrados
        if (clientetoInsert == null || quartotoInsert == null) {
            throw new RuntimeException("Cliente ou Quarto n√£o encontrados!");
        }

        // Criar nova reserva
        Reserva NovaReserva = new Reserva();
        NovaReserva.setCliente(clientetoInsert);
        NovaReserva.setQuarto(quartotoInsert);
        NovaReserva.setDataInicio(LocalDate.parse(jTextField1.getText()));
        NovaReserva.setDataFim(LocalDate.parse(jTextField2.getText()));
        NovaReserva.setStatus("Reservada");

        // Persistir a reserva
        repository.persist(NovaReserva);
        JOptionPane.showMessageDialog(this, "Reserva cadastrada com sucesso!");
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

